#include<iostream> 
#include<math.h> 
using namespace std;
int main()
{
	double x, y;
	cout <<"Ingrese x: ";
	cin>>x;
	
	cout <<"Ingrese y: ";
	cin>>y;
	
	cout<<" "<<endl;
	
	cout <<"ceil= "<<ceil (x)<<endl;
	cout <<"cos ="<<cos (x)<<endl;
	cout <<"exp ="<<exp (x)<<endl;
	cout <<"fabs = "<<fabs (x)<<endl;
	cout <<"floor ="<<floor (x)<<endl;
	cout <<"log ="<<log (x)<<endl;
	cout <<"log ="<<log10 (x)<<endl;
	cout <<"sin ="<<sin (x)<<endl;
	cout <<"sqrt ="<<sqrt (x)<<endl;
	cout <<"tan ="<<tan (x)<<endl;
	cout <<"fmod ="<<fmod (x,y)<<endl;
	cout <<"pow ="<<pow (x,y)<<endl;
}
