// Programa para que se comparen los numeros
#include<iostream>
using namespace std;
int main()
{
	int x,y;
	cout<<"Ingrese un numero"<<endl;
	cin>>x;
	cout<<"Ingrese otro numero"<<endl;
	cin>>y;
	if(x==y){
	cout<<"Sus valores 1 y 2 son iguales"<<endl;	
	}
	else if(x>y){
		cout<<"Su primer valor es mayor al segundo"<<endl;
	}
	else{
		cout<<"Su segundo valor es mayor al primero"<<endl;
	}
		
	

	
	
}
